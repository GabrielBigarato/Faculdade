// Declaração de uma classe chamada LacosRepeticaoExemplo
public class LacosRepeticaoExemplo {
    // Método principal, o ponto de entrada do programa Java
    public static void main(String[] args) {
        // Inicializa uma variável chamada 'i' com o valor 1
        // O loop continuará enquanto 'i' for menor ou igual a 5
        // A cada iteração, 'i' é incrementado em 1
        for (int i = 1; i <= 5; i++) {
            // Imprime uma mensagem indicando a iteração atual
            System.out.println("Iteração " + i);
        }
    }
}